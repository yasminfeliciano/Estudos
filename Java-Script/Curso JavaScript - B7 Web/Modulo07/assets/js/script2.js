//MÉTODOS DE NUMBERS
//Para transformar um 'number' em 'string' usa-se 'toString'.
let n = 10;

let resString = n.toString()+5;

console.log(resString);

//Para arredondar um número usa-se 'toFixed' com um parâmetro para determinar a quantidade de casas decimais.
let n2 = 10.654738;

let resFixed = n.toFixed(2);

console.log(resFixed);

//Para transformar uma 'string' em 'number int' usa-se 'parseInt'.
let n3 = '25';

let resParseInt = parseInt(n3)+5;

console.log(resParseInt);

//Para transformar uma 'string' em 'number float' usa-se 'parseFloat'.
let n4 = '25.9';

let resParseFloat = parseFloat(n4)+5;

console.log(resParseFloat);