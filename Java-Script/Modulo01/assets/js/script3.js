//As linhas de código do JavaScript não necessitam de ponto e vírgula para funcionar, somente será necessário o uso de ponto e vírgula caso haja mais de um código sendo executado na mesma linha.
let nome ="Yasmin"; let idade = 25
console.log(nome); console.log(idade)


//Para alterar uma variável basta apenas colocar o nome e em seguida o novo valor. Não é necessário o "let".
nome = "Victor"
console.log(nome)

//tanto o "let" como o "var" podem ser usados para a criação de variáveis.
//O "var" é uma variável que funciona em qualquer contexto. Já o "let", apenas funciona no contexto em que foi criada
//O "const" é uma constante, ou seja, seu valor não irá mudar